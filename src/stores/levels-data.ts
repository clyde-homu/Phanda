export const getLevel = (language: string, id: number): GameLevel | undefined => {
  return GAME_LEVELS[language]?.find((level) => level.id === id);
};

export const getUnlockedLevels = (language: string, completedLevels: number[]): GameLevel[] => {
  const levels = GAME_LEVELS[language] || [];
  return levels.map((level) => ({
    ...level,
    isUnlocked: level.id === 1 || completedLevels.includes(level.id - 1),
    isCompleted: completedLevels.includes(level.id),
  }));
};

export const getUnlockedLanguages = (): Language[] => {
  return AVAILABLE_LANGUAGES.map((lang) => ({
    ...lang,
    isUnlocked: true,
  }));
};
